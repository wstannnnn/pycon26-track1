def test_analyze_learner_profile(monkeypatch) -> None:
    async def fake_similarity_search(text: str, role_query: str = "") -> list[VectorSearchHit]:
        assert "Skillset: SQL, Excel, stakeholder interviews" in text
        assert role_query == "Data Analyst"
        return [
            VectorSearchHit(
                id="business-analyst",
                score=0.92,
                payload={
                    "role": "Business Analyst",
                    "skills": ["SQL", "Process mapping", "Dashboarding"],
                    "source": "test-taxonomy",
                },
            )
        ]

    monkeypatch.setattr(
        "app.routers.learner.similarity_search",
        fake_similarity_search,
    )

    async def fake_generate_recommendation(profile_text, matches):
        return (
            LearnerRecommendation(
                next_roles=["Data Analyst"],
                priority_skills=["SQL", "Dashboarding"],
                actions_today=["Build one dashboard."],
                explanation="Generated by local LLM.",
            ),
            "local",
        )

    monkeypatch.setattr(
        "app.routers.learner.generate_recommendation",
        fake_generate_recommendation,
    )

    with TestClient(app) as client:
        response = client.post(
            "/learner/analyze",
            json={
                "current_role": "Customer Support Specialist",
                "target_interest": "Data Analyst",
                "skillset": "SQL, Excel, stakeholder interviews",
                "resume_text": "Handled customer escalations and weekly reporting.",
            },
        )

    assert response.status_code == 200
    body = response.json()
    assert body["llm_provider"] == "local"
    assert body["similar_matches"][0]["id"] == "business-analyst"
    assert body["recommendation"]["next_roles"][0] == "Data Analyst"
    assert "SQL" in body["recommendation"]["priority_skills"]



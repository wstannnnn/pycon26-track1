def test_build_recommendation_prompt_compacts_large_vector_payloads() -> None:
    prompt = build_recommendation_prompt(
        profile_text="Resume: " + ("large resume " * 600),
        matches=[
            VectorSearchHit(
                id="data-analyst",
                score=0.91,
                payload={
                    "role": "Data Analyst",
                    "skills": ["SQL", "Python"],
                    "description": "Analyse datasets and build dashboards.",
                    "document": "very large indexed document " * 600,
                    "documents": ["another giant document " * 600],
                },
            )
        ],
    )

    assert "Data Analyst" in prompt
    assert "SQL" in prompt
    assert "Analyse datasets and build dashboards." in prompt
    assert "very large indexed document" not in prompt
    assert len(prompt) < 5_000


def test_compress_evidence_groups_by_role() -> None:
    compressed = compress_evidence(
        [
            VectorSearchHit(
                id="data-analyst-sql",
                score=0.9,
                payload={
                    "role": "Data Analyst",
                    "skill": "SQL",
                    "description": "Query datasets.",
                },
            ),
            VectorSearchHit(
                id="data-analyst-python",
                score=0.8,
                payload={
                    "role": "Data Analyst",
                    "skill": "Python",
                    "task": "Build dashboards",
                },
            ),
        ]
    )

    assert len(compressed) == 1
    assert compressed[0]["role"] == "Data Analyst"
    assert compressed[0]["skills"] == ["SQL", "Python"]
    assert compressed[0]["tasks"] == ["Build dashboards"]



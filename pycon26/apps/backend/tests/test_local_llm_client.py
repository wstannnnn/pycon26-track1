import json

import httpx
import pytest

from app.clients.local_llm import LocalLlmClient, build_recommendation_prompt, compress_evidence
from app.schemas.vectors import VectorSearchHit


@pytest.mark.anyio
async def test_local_llm_client_parses_ollama_json_response() -> None:
    requests: list[httpx.Request] = []

    async def handler(request: httpx.Request) -> httpx.Response:
        requests.append(request)
        return httpx.Response(
            200,
            json={
                "response": json.dumps(
                    {
                        "next_roles": ["Data Analyst"],
                        "priority_skills": ["SQL", "Python"],
                        "actions_today": ["Build a dashboard from a small dataset."],
                        "explanation": "Matched against analyst evidence.",
                    }
                )
            },
        )

    client = LocalLlmClient(
        base_url="http://local-llm.test",
        model="llama-test",
        provider="ollama",
        enabled=True,
        transport=httpx.MockTransport(handler),
    )

    recommendation = await client.recommend(
        profile_text="Skillset: SQL",
        matches=[
            VectorSearchHit(
                id="data-analyst",
                score=0.91,
                payload={"role": "Data Analyst", "skills": ["SQL"]},
            )
        ],
    )

    assert recommendation is not None
    assert recommendation.next_roles == ["Data Analyst"]
    assert recommendation.priority_skills == ["SQL", "Python"]
    assert requests[0].method == "POST"
    assert requests[0].url.path == "/api/generate"
    assert json.loads(requests[0].read())["model"] == "llama-test"


